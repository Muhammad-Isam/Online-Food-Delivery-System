    private void cbaddressItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbaddressItemStateChanged
        if (cbaddress.getSelectedItem().toString().equals("Add new address")) {
            cbaddress.getItemCount();
            cbaddress.removeItemAt(cbaddress.getItemCount() - 1);
            String v = JOptionPane.showInputDialog(null, "Enter new Address to deliver");
            cbaddress.addItem(v);
            cbaddress.setSelectedItem(v);
            u.addUserAddress(u.getuEmail(), v);
            cbaddress.addItem("Add new address");

        }
    }//GEN-LAST:event_cbaddressItemStateChanged

    private void btnplaceorderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnplaceorderActionPerformed
        u.placeOrder(lblccode.getText());
        int row;
        for (int i = 0; i < tblmenu.getRowCount(); i++) {
            String mn, qty, price, subtotal;
            row = i;
            qty = tblmenu.getModel().getValueAt(row, 0).toString();
            mn = tblmenu.getModel().getValueAt(row, 1).toString();
            price = tblmenu.getModel().getValueAt(row, 2).toString();
            subtotal = tblmenu.getModel().getValueAt(row, 3).toString();
            u.placeItemOrder(mn, Float.valueOf(price), Integer.parseInt(qty),
                    Float.valueOf(subtotal));
        }
        u.confirmCartOrder(cbaddress.getSelectedItem().toString(), lblrname.getText(), lblradd.getText(), u.getuEmail(),cbpmethod.getSelectedItem().toString());
    }//GEN-LAST:event_btnplaceorderActionPerformed

