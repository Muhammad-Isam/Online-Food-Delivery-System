    public void loadTable() {
        lblrname.setText(rg.getLblrname());
        lblradd.setText(rg.getLblradd());
        DefaultTableModel model1 = (DefaultTableModel) tblmenu.getModel();

        for (int i = 0; i < rg.mList.size(); i++) {
            Menu mobj;
            mobj = rg.mList.get(i);
            if (mobj.getQty() != 0) {
                model1.addRow(new Object[]{mobj.getQty(), mobj.getiName(), mobj.getiPrice(), mobj.getItemSubTotal()});
            }
        }
    }

    public ArrayList<Menu> getCartItems() {
        ArrayList<Menu> cartItems = new ArrayList<>();
        int column = 0;
        int row = 0;
        Menu m = new Menu();
        for (int i = 0; i < tblmenu.getRowCount(); i++) {

            row = i;
            m.setQty(Integer.parseInt(tblmenu.getModel().getValueAt(row, 0).toString()));
            m.setiName((tblmenu.getModel().getValueAt(row, 1).toString()));
            m.setiPrice(Float.parseFloat(tblmenu.getModel().getValueAt(row, 1).toString()));
            m.setItemSubTotal(Float.parseFloat(tblmenu.getModel().getValueAt(row, 1).toString()));
            cartItems.add(m);
        }
        return cartItems;
    }

    public void setTotals() {
        float subTotal = 0;
        float Total = 0;
        for (int i = 0; i < rg.mList.size(); i++) {
            Menu mobj;
            mobj = rg.mList.get(i);
            if (mobj.getQty() != 0) {
                subTotal += mobj.getItemSubTotal();
            }
        }
        Total = subTotal - Float.parseFloat(lbldiscount.getText());
        lblsubtotal.setText(String.valueOf(subTotal));
        lbltotal.setText(String.valueOf(Total));
    }

